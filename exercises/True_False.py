number_1 = 75 # Variable with an int inside it
number_2 = 100 
number_3 = number_2 - 25 # Variable with an take away calculation inside. Whatever value is inside number_2 take away 25. 
print(number_1 == number_2) # Output: False
print(number_3 == number_1) # Output: True
print(number_1 != number_2) # Output: True
print(number_3 != number_1) # Output: False
